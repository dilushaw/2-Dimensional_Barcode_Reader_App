#
# Structure for the `role` table : 
#

DROP TABLE IF EXISTS `role`;

CREATE TABLE `role` (
  `role_id` varchar(25) NOT NULL DEFAULT 'anuradha',
  `role_type` varchar(20) NOT NULL,
  PRIMARY KEY (`role_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

#
# Structure for the `lob_group` table : 
#

DROP TABLE IF EXISTS `lob_group`;

CREATE TABLE `lob_group` (
  `lob_id` int(10) unsigned NOT NULL,
  `lob_name` varchar(20) NOT NULL,
  `created_user_id` bigint(20) NOT NULL,
  PRIMARY KEY (`lob_id`),
  UNIQUE KEY `lob_id` (`lob_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

#
# Structure for the `corporate` table : 
#

DROP TABLE IF EXISTS `corporate`;

CREATE TABLE `corporate` (
  `corporate_id` int(10) NOT NULL AUTO_INCREMENT,
  `corporate_account` varchar(20) DEFAULT NULL,
  `address` varchar(20) DEFAULT NULL,
  `telephone` int(10) unsigned DEFAULT NULL,
  `fax` int(10) unsigned DEFAULT NULL,
  `email` varchar(20) DEFAULT NULL,
  `contact_name` varchar(20) DEFAULT NULL,
  `license_start_date` datetime DEFAULT NULL,
  `license_end_date` datetime DEFAULT NULL,
  `corporate_name` varchar(30) DEFAULT NULL,
  `corporate_status` int(1) DEFAULT NULL,
  `description` varchar(200) DEFAULT NULL,
  PRIMARY KEY (`corporate_id`)
) ENGINE=InnoDB AUTO_INCREMENT=12 DEFAULT CHARSET=latin1;

#
# Structure for the `user` table : 
#

DROP TABLE IF EXISTS `user`;

CREATE TABLE `user` (
  `user_id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `user_name` varchar(20) NOT NULL,
  `password` varchar(20) NOT NULL,
  `corporate_id` int(10) NOT NULL,
  `name` varchar(20) NOT NULL,
  `mobile` int(10) unsigned NOT NULL,
  `email` varchar(20) NOT NULL,
  `description` varchar(20) NOT NULL,
  `role_id` varchar(25) NOT NULL,
  `lob_id` int(10) unsigned DEFAULT NULL,
  `last_login_date` datetime NOT NULL,
  PRIMARY KEY (`user_id`),
  KEY `role_id` (`role_id`),
  KEY `corporate_id` (`corporate_id`),
  KEY `lob_id` (`lob_id`),
  CONSTRAINT `role_id` FOREIGN KEY (`role_id`) REFERENCES `role` (`role_id`),
  CONSTRAINT `user_fk` FOREIGN KEY (`lob_id`) REFERENCES `lob_group` (`lob_id`),
  CONSTRAINT `user_fk1` FOREIGN KEY (`corporate_id`) REFERENCES `corporate` (`corporate_id`)
) ENGINE=InnoDB AUTO_INCREMENT=25 DEFAULT CHARSET=latin1;

#
# Structure for the `barcode` table : 
#

DROP TABLE IF EXISTS `barcode`;

CREATE TABLE `barcode` (
  `barcode_id` varchar(25) NOT NULL DEFAULT 'anuradha',
  `user_id` bigint(20) unsigned NOT NULL,
  `date_created` datetime NOT NULL,
  `type` varchar(20) NOT NULL,
  `bc_action_id` varchar(20) NOT NULL,
  `imge_type` varchar(20) NOT NULL,
  `error_correction_level` varchar(20) NOT NULL,
  `exp_date` datetime NOT NULL,
  `size_id` varchar(25) NOT NULL,
  PRIMARY KEY (`barcode_id`),
  KEY `size_id` (`size_id`),
  KEY `user_id` (`user_id`),
  CONSTRAINT `size_id` FOREIGN KEY (`size_id`) REFERENCES `sizes` (`size_id`),
  CONSTRAINT `user_id` FOREIGN KEY (`user_id`) REFERENCES `user` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

#
