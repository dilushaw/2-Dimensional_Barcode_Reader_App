#
# Structure for the `sizes` table : 
#

DROP TABLE IF EXISTS `sizes`;

CREATE TABLE `sizes` (
  `size_id` varchar(25) NOT NULL DEFAULT 'anuradha',
  `width` int(10) unsigned NOT NULL,
  `length` int(10) unsigned NOT NULL,
  PRIMARY KEY (`size_id`)
